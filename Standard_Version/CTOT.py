import sympy as sy
from sympy import log, exp, ccode
import importlib
import sys
import os


#check if Equation.py file name is given as console argument

if(len(sys.argv) <= 1):
	print("Error, Equations.py file name not given as console argument. Use python3 CTOT.py <name of Equations file>.")
	exit()

file = sys.argv[1]


#filename of the Equations.py file 
filename = file.split(".py")[0]


#check if Equations.py file exists
if(os.path.isfile(file) == False):
	print("Error, file %s does not exist." % file)
	exit()

#import Equations file with the name given as console argument
Equations = importlib.import_module(filename)




print("Using chemical network equations from file %s.py" % filename)
print("")



Equations.printff()

ff, P, variables, Parameters, nGRT  = Equations.getff()


nF = len(ff)

var = sy.IndexedBase('var', (nF,), real=True)
logvar = sy.IndexedBase('logvar', (nF,), real=True)     #This is a helper array to speed up the calculations



# ************************************************************
# Define the cost function CTOT and its gradients
# The cost function is the sum of the squares of all equations
# ************************************************************
#square of functions
ff2 = []
for f in ff:
        ff2.append(f**2)


# Costfunction
CTOT = sum(ff2)
# ************************************************************


# ***************************************************************
# Calculate the gradients of CTOT with respect to all variables
# We solve the equations in log space, therefore we need the
# derivatives in repect of the log of the variables

# df/d(log(x)) = df/dx * dx/d log(x) = df/dx * x
# ***************************************************************

print("Calculating symbolic derivatives")
useLog = 1		#0: calcualte the derivatives in respect of the values, 1: in respect of the log of the values

if(useLog == 1):
	CTOT_grad = [sy.diff(CTOT, var[i]) * var[i] for i in range(nF)] #the '*var[i]' is the inner derivative of the logarithm
else:
	CTOT_grad = [sy.diff(CTOT, var[i]) for i in range(nF)]

# ***************************************************************






#replace log(var) with the variables logvar before printing, this will speed up the calculations
for i in range(nF):
	ff[i] = ff[i].subs((log(var[i]), logvar[i]) for i in range(nF))
	#print(ccode(ff[i]))
	#print("")

for i in range(nF):
	CTOT_grad[i] = CTOT_grad[i].subs((log(var[i]), logvar[i]) for i in range(nF))

	#print(CTOT_grad[i])
	#print("")



# *********************************************************************************
# Now gnerate the c++ code of the costfunstion and the derivatives
# *********************************************************************************




# *********************************************************************************
# generate ctot.cpp file
# *********************************************************************************
filename1 = 'ctot.cpp'
file1 = open(filename1, 'w')

print('//This file is automatically generated by the python3 script CTOT.py', file = file1)
print('//This code is generated with SymPy and is using Common Subexpression Elimination (CSE) to speed up the code', file = file1)
print('', file = file1)

print('#include "../src/solver.h"', file = file1)
print('double solver::ctot(double *var, double *logvar, double P){', file = file1)
print('', file = file1)

print('\tdouble f[%d];' % nF, file = file1)
print('', file = file1)

#Use SymPy common subexpression elimination to optimize the cost function code
commons, exprs = sy.cse((ff))

names = []
for i in range(nF):
	names.append("f[%d]" % i)

print("\t//common expressions:", file = file1)

for com in commons:
	print('\tdouble ',ccode(com[0]), ' = ', ccode(com[1]), ';', sep='', file = file1)

print("", file = file1)

print("\t//reduced expressions:", file = file1)
for expr, name in zip(exprs, names):
	print('\t', name, ' = ', ccode(expr), ';', sep='', file = file1)


print("", file = file1)
print("", file = file1)
print("\t//Square all f and add them up", file = file1)
print("\tdouble CTOT = 0.0;", file = file1)
print("\tfor(int i = 0; i < Ndim; ++i){", file = file1)
print("\t\tCTOT += f[i] * f[i];", file = file1)
print("\t}", file = file1)
print("", file = file1)
print("", file = file1)
print('\treturn CTOT;', file = file1)
print('}', file = file1)

file1.close()
print("generated %s file" % filename1)
