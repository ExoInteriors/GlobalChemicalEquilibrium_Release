# The filename of the equations file must be given as console argument e.g. 'python3 CTOT.py Equations.py'
# The script used SymPy's symbolic algebra package to compute the derivatives of the cost function.
# Every parameter must be defined as sympy symbol
# The script uses SymPy's common subexpression elimination (CSE) technique to simplify the equations and derivatives for an optimized code
# At the end, it generates a C++ code, that can be compiled.

